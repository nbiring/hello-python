# This is my sample file.
def add(a, b): # Addition Function
	return a + b


def multiply(a, b): # Multipy Function
	return a * b


def divide(a, b): # Divide Function
	return a / b


def subtract(a, b): # Subtract Function
	return a - b


print('2 + 3 = %d' % add(2, 3))
print('2 * 5 = %d' % multiply(2, 5))
print('5 - 2 = %d' % subtract(5, 2))
