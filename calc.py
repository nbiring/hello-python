# This is my sample file.
def add(a, b):
	return a + b


def multiply(a, b):
	return a * b


def divide(a, b):
	return a / b


def subtract(a, b):
	return a - b


print('2 + 3 = %d' % add(2, 3))
print('2 * 5 = %d' % multiply(2, 5))
print('5 - 2 = %d' % subtract(5, 2))
print('4 / 2 = %d' % divide(4, 2))
